## These two functions create a matrix with a cached inverse
## that can be used much like methods on a class.
## 

## The makeCacheMatrix function takes a maxtrix as an input
## and provides a method  for updating (set) and accessing (get)
## both the matirix and it's inverse. 

makeCacheMatrix <- function(x = matrix()) {
        inv <- NULL
        set <- function(y) {
                x <<- y
                inv <<- NULL
        }
        get <- function() x
        setinv <- function(i) inv <<- i
        getinv <- function() inv
        list (set = set, get = get, setinv = setinv, getinv = getinv)
}


